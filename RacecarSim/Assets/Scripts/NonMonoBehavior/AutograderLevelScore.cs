/// <summary>
/// Encapsulates the user's performance on a single autograder level.
/// </summary>
public class AutograderLevelScore
{
    /// <summary>
    /// The total score which the user received on the autograder level.
    /// </summary>
    public float Score;

    /// <summary>
    /// The time in seconds which it took to complete the autograder level.
    /// </summary>
    public float Time;
}